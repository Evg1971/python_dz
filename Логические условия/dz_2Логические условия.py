num = int(input("Введите число от 1 до 5: "))
if num == 1:
    print("Соответствующее слово: One")
elif num == 2:
    print("Соответствующее слово: Two")
elif num == 3:
    print("Соответствующее слово: Three")
elif num == 4:
    print("Соответствующее слово: Four")
elif num == 5:
    print("Соответствующее слово: Five")
else:
    print("Надо ввести число от 1 до 5")
